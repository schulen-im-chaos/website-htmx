<h2 class="h2">Unser Ziel</h2>
<p>
	Das Ziel unseres Projekts "Schulen im Chaos" ist es, mithilfe von vielen Schülern von den
	verschiedensten Schulen in Deutschland, eine Plattform zu schaffen, auf welcher Schüler anderen
	Schülern bei z.B. Hausaufgaben helfen können. Dies soll nach dem Prinzip "Schüler helfen Schülern"
	funktionieren, allerdings auf freiweilliger und kostenloser Basis. Das Ziel dahinter ist es, so
	vielen Schülerinnen und Schülern die Möglichkeit zu geben, sich in der Schule zu verbessern, auch
	wenn z.B. die finanzielle Lage der Familie nicht ausreicht, um Nachhilfe zu finanzieren, oder die
	Förderungsmöglichkeiten der Schulen nicht ausreichen.
</p>
<h2>Positiver Nebeneffekt</h2>
<p>
	Das Projekt hat auch noch einen positiven Nebeneffekt, nämlich das sich die Schüler
	verschiedenster Schulen miteinander austauschen können, wenn sich z.B. ein Schüler fragt, ob eine
	Situation, welche er in irgendeiner Weise vorfindet, an anderen Schulen auch besteht, und auch
	vieles mehr. Auch für unterhaltsame Gespräche außerhalb des Themas Schule ist unser Projekt
	natürlich möglich, hierfür gibt es gesonderte Text- und Sprachkanäle.
</p>
